"""Template builder."""

from . import *  # noqa: F403, F401


def build_template() -> Template:
    """Build the CloudFormation template."""
    return Template.from_registry(
        description="""Creates a static website using S3 and CloudFront that passes typical
compliance checks.  Generated by CloudFormation Rain (rain build -b bucket
website). Adapt this template to your needs and thoruoughly test it before
introducing it in a production environment. **WARNING** This template will
create resources in your account that may incur billing charges.
""",
        parameters=[AppName, WebACL],
        outputs=[SiteURLOutput],
    )


def main() -> None:
    """Print the CloudFormation template as JSON."""
    import json
    template = build_template()
    print(json.dumps(template.to_dict(), indent=2))


if __name__ == "__main__":
    main()
